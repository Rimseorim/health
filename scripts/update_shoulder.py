    path = os.path.join(MOVEMENTS_DIR, filename)
    data = json.load(open(path, encoding='utf-8'))
    replaced = False
    for i, ps in enumerate(data['pain_sites']):
        if ps['id'] == 'shoulder':
            data['pain_sites'][i] = SHOULDER
            replaced = True
            break
    if replaced:
        with open(path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        print(f"  updated {filename}")
    else:
        print(f"  shoulder not found in {filename}")

